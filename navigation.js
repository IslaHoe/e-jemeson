
function chageBackground(x) {
    
  if (x == 1){
    document.body.style.backgroundColor = "#327272";
  } 

  if (x == 2){
    document.body.style.backgroundColor = "#326eb4";
  }

  if (x == 3) {
     document.body.style.backgroundColor = "#e37774";
  }

  if (x == 4) {
     document.body.style.backgroundColor = "#fff096";
  }
}
